#include "libft.h"

void	*ft_bzero(void *ptr, size_t count)
{
	size_t	i;
	char	*ptr2;

	ptr2 = ptr;
	i = 0;
	while (i != count)
	{
		ptr2[i] = '\0';
		i++;
	}
	return (ptr2);
}
