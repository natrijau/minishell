#include "libft.h"

int	ft_isalnum(int car)
{
	if ((car >= '0' && car <= '9')
		|| (car >= 'A' && car <= 'Z')
		|| (car >= 'a' && car <= 'z'))
		return (1);
	else
		return (0);
}
